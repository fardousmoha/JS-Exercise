let a =15;
let b ="15";


console.log(a == b);  // Output: true (loose equality)
console.log(a != b);  // Output: false



console.log(a === b); // Output: false (strict equality)
console.log(a !== b); // Output: true



console.log(a > b);   // Output: false
console.log(a < b);   // Output: false
console.log(a >= b);  // Output: true
console.log(a <= b);  // Output: true
